    private void btnFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFichaActionPerformed
        // TODO add your handling code here:
       String Nombre_usuario=txtNombre.getText();
       Nombre.setText(Nombre_usuario);
       String paquete="";
       String paquete_seleccionado= (String) cbxtPaquete.getSelectedItem();
       
       if(paquete_seleccionado.equals("No perecederos")){
           paquete="NP";
        }
       if(paquete_seleccionado.equals("Perecederos")){
       paquete="P";
       }
       
       String usuario_seleccionado= (String) cbxtUsuario.getSelectedItem();
       String usuario = "";
       if(usuario_seleccionado.equals("Discapacitado")){
           usuario="D";
       }
       if(usuario_seleccionado.equals("Regular")){
           usuario="R";
       }
       if(usuario_seleccionado.equals("Embarazada")){
         usuario="E";  
       }
       if(usuario_seleccionado.equals("Adulto Mayor")){
           usuario="M";
       }
       
       int Numero_Tiquete=this.Contador;
       Ficha.setText(paquete+"-"+usuario+"-"+Numero_Tiquete);
       
       
    }//GEN-LAST:event_btnFichaActionPerformed

