    private void btnAñadirPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirPActionPerformed
        // TODO add your handling code here:
        int Cantidad= (Integer.parseInt(Cant_Perecederos.getText()));
        int i;
        System.out.println(Cantidad);
        DefaultTableModel TablaPerecederos= (DefaultTableModel) Perecederos_Ventana.getModel();
        int Inicio=ActualPerecederos; 
        for(i=Cantidad;i>0;i--){
            System.out.println(i);
            TablaPerecederos.addRow(new Object[]{"#"+Inicio,"Libre"});
            Inicio++;
            ActualPerecederos=Inicio;
        }
    }//GEN-LAST:event_btnAñadirPActionPerformed

